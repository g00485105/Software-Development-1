public class FirstIfs {
    public static void main(String[] args) {
        int x = 10;

        if (x == 1) {
            System.out.println(x + " is equal to 1");
        }
        if (x != 1) {
            System.out.println("x is greater than y");
        } 
        if (x > 1) {
            System.out.println("x is greater than y");
        } 
         if (x < 1) {
            System.out.println("x is greater than y");
        } 
    }
}